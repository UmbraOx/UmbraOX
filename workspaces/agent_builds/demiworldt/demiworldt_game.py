            for dy in range(-rad,rad+1):
                for dx in range(-rad,rad+1):
                    if dx*dx+dy*dy <= rad*rad:
                        x2,y2 = cx+ox+dx, cy+oy+dy
                        if 0<=x2<MAP_W and 0<=y2<MAP_H:
                            wmap[y2][x2] = btype


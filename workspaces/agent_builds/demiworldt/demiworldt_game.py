class Projectile:
    def __init__(self, x, y, tx, ty, dmg, col, speed=8):
        self.x=float(x); self.y=float(y)
        d=math.hypot(tx-x,ty-y) or 1
        self.vx=(tx-x)/d*speed; self.vy=(ty-y)/d*speed
        self.dmg=dmg; self.col=col; self.alive=True
        self.life=120

    def update(self):
        self.x+=self.vx; self.y+=self.vy
        self.life-=1
        if self.life<=0: self.alive=False

    def draw(self, surf, cam_x, cam_y):
        pygame.draw.circle(surf, self.col, (int(self.x-cam_x),int(self.y-cam_y)), 5)



# ── Helper: draw text ─────────────────────────────────────────────────────────
_font_cache = {}
def font(size):
    if size not in _font_cache:
        _font_cache[size] = pygame.font.SysFont("segoeui", size)
    return _font_cache[size]

def txt(surf, text, x, y, size=16, col=WHITE, center=False):
    f = font(size)
    s = f.render(str(text), True, col)
    if center:
        x -= s.get_width()//2
    surf.blit(s, (x,y))
    return s.get_width(), s.get_height()

def draw_bar(surf, x, y, w, h, val, mx, col, bg=DKGRAY):
    pygame.draw.rect(surf, bg, (x,y,w,h))
    fill = int(w * max(0, val/max(mx,1)))
    if fill > 0:
        pygame.draw.rect(surf, col, (x,y,fill,h))
    pygame.draw.rect(surf, GRAY, (x,y,w,h), 1)

def draw_x_button(surf, rx, ry, rw, rh, col=WHITE):
    """Draw an X close button in top-right of rect. Returns button rect."""
    btn = pygame.Rect(rx+rw-26, ry+4, 22, 22)
    pygame.draw.rect(surf, DKRED, btn, border_radius=4)

class Entity:
    def __init__(self, x, y, col, size=16):
        self.x = float(x*TILE + TILE//2)
        self.y = float(y*TILE + TILE//2)
        self.col = col
        self.size = size
        self.vx = self.vy = 0.0

    def draw(self, surf, cam_x, cam_y):
        sx = int(self.x - cam_x)
        sy = int(self.y - cam_y)
        if -self.size*2 <= sx <= SW+self.size*2 and -self.size*2 <= sy <= SH+self.size*2:
            pygame.draw.circle(surf, self.col, (sx,sy), self.size)

    @property
    def tile_x(self): return int(self.x // TILE)
    @property
    def tile_y(self): return int(self.y // TILE)



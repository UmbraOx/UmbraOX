TILE_SURF_CACHE={}
def get_tile_surf(btype):
    if btype not in TILE_SURF_CACHE:
        s=pygame.Surface((TILE,TILE))
        col=BIOME_COL.get(btype,(100,100,100))
        s.fill(col)
        # Add texture variation
        if btype=="forest":
            for _ in range(4):
                rx=random.randint(2,TILE-6); ry=random.randint(2,TILE-6)
                pygame.draw.circle(s,(20,80,20),(rx,ry),3)
        elif btype=="mountain":
            pygame.draw.polygon(s,(160,140,120),[(TILE//2,4),(4,TILE-4),(TILE-4,TILE-4)])
        elif btype=="water":
            pygame.draw.line(s,(60,100,200),(2,TILE//2),(TILE-2,TILE//2),2)
        elif btype=="mine":
            pygame.draw.rect(s,(80,70,60),(6,6,TILE-12,TILE-12))
        elif btype=="wood_area":
            pygame.draw.rect(s,(40,100,40),(8,8,TILE-16,TILE-16))
        TILE_SURF_CACHE[btype]=s
    return TILE_SURF_CACHE[btype]



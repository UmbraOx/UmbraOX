from typing import List, Dict

class ConversationHistory:
    """
    A class to maintain conversation history and preserve context across turns.
    """

    def __init__(self):
        self.conversation_log: List[Dict[str, str]] = []

    def add_turn(self, user_input: str, bot_response: str) -> None:
        """
        Adds a turn to the conversation log.

        Args:
            user_input (str): The input provided by the user.
            bot_response (str): The response generated by the bot.
        """
        self.conversation_log.append({
            "user": user_input,
            "bot": bot_response
        })

    def get_conversation_history(self) -> List[Dict[str, str]]:
        """
        Returns the entire conversation history.

        Returns:
            List[Dict[str, str]]: A list of dictionaries containing each turn in the conversation.
        """
        return self.conversation_log

# Example usage:
if __name__ == "__main__":
    # Initialize conversation history
    conversation_history = ConversationHistory()

    # Simulate a few turns in a conversation
    try:
        conversation_history.add_turn("Hello", "Hi there!")
        conversation_history.add_turn("How are you?", "I'm just a bot, but I'm here and ready to help.")
        conversation_history.add_turn("What's your name?", "You can call me Qwen.")

        # Retrieve and print the conversation history
        history = conversation_history.get_conversation_history()
        for turn in history:
            print(f"User: {turn['user']}")
            print(f"Bot: {turn['bot']}\n")

    except Exception as e:
        print(f"An error occurred: {e}")
